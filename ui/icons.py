def icon(name: str, color: str = "#475569", size: int = 22) -> QIcon:
    """渲染指定名称的 SVG 图标为 QIcon。color 为描边色。"""
    path = _PATHS.get(name)
    if not path:
        return QIcon()
    svg = (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{size}" height="{size}" '
        f'viewBox="0 0 24 24" fill="none" stroke="{color}" stroke-width="2" '
        f'stroke-linecap="round" stroke-linejoin="round">{path}</svg>'
    )
    renderer = QSvgRenderer(bytearray(svg.encode("utf-8")))
    pm = QPixmap(size, size)
    pm.fill(Qt.transparent)
    painter = QPainter(pm)
    renderer.render(painter)
    painter.end()
    return QIcon(pm)


def pixmap(name: str, color: str = "#475569", size: int = 22) -> QPixmap:
    return icon(name, color, size).pixmap(QSize(size, size))
